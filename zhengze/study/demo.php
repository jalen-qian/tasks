function is_email($email)
{
	return preg_match("/^[a-zA-Z_.]+@[a-zA-Z_.]+\.[a-zA-Z]+$/", $email);
}

echo is_email("tom@php.net");   //true
echo is_email("tom@php");   //false
echo "<br>";
function isnumber($num){
	/*
	 * 在PCRE中，通常将正则表达式 包含在分隔符两个反斜线“/”之间;
	 * 例：preg_match(‘/[a-z0-9]/i’, $str);
	 * 分隔符也不仅仅局限于“/”。除了字母、数字和斜线“\”以外的任何字符都可以作为分隔符，像“#”、“/”、“!”等都可以的。
	 */
	return preg_match("^[0-9]+$^",$num);
}
echo isnumber("55555");


/*
 * 电话号码：
 * 以区号（3-4位开头）- （数字 7-8位）
 */
function isPhoneNumber($num){
	return preg_match("/^[0-9]{3,4}-[0-9]{7,8}$/",$num);
}
echo isPhoneNumber("4410-8862231");
